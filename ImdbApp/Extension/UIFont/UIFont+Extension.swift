import UIKit

extension UIFont {
    class func postGroteskBold(_ size: CGFloat) -> UIFont? {
        return UIFont(name: "PostGrotesk-Bold", size: size)
    }
    
    class func postGroteskBook(_ size: CGFloat) -> UIFont? {
        return UIFont(name: "PostGrotesk-Book", size: size)
    }
}
